#!/usr/bin/env node
// Generates placeholder icon files for Electron builder
const fs = require('fs');
const path = require('path');

const buildDir = path.join(__dirname, '..', 'src', 'electron-app', 'build');
if (!fs.existsSync(buildDir)) fs.mkdirSync(buildDir, { recursive: true });

// Generate a minimal 256x256 PNG (valid PNG with a single blue pixel)
// In production, replace this with a real logo
const iconPath = path.join(buildDir, 'icon.png');
if (!fs.existsSync(iconPath)) {
  // Minimal valid PNG: 1x1 blue pixel
  // For production, create a real 256x256 icon and place it at build/icon.png
  const png = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
    0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, // IDAT chunk
    0x54, 0x08, 0xD7, 0x63, 0x60, 0x60, 0x60, 0x00,
    0x00, 0x00, 0x04, 0x00, 0x01, 0x27, 0x34, 0x0A,
    0x0E, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, // IEND chunk
    0x44, 0xAE, 0x42, 0x60, 0x82
  ]);
  fs.writeFileSync(iconPath, png);
  console.log(`Icono placeholder generado: ${iconPath}`);
  console.log('⚠️  Reemplazar con un logo real de 256x256 PNG antes de publicar.');
}
